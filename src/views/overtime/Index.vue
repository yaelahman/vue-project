<script>
import moment from "moment";
import * as Api from "../../helper/Api.js";
import Swal from "sweetalert2";
import { GoogleMap, Marker, MarkerCluster } from "vue3-google-map";
export default {
  data() {
    return {
      api_key: env.VITE_API_GMAPS,
      overtimes: {},
      personels: {},
      table: null,
      edit: null,
      search: {
        t_absensi_Dates: "",
        startDate: "",
      },
      modal: {
        id: "",
        nama: "",
        startDate: "",
        startClock: "",
        endDate: "",
        endClock: "",
        personel: "",
        photo: "",
        photo2: "",
        status_admin: "",
        catatan_admin: "",
      },
      data: {
        catatan: "",
        type: "",
        id: "",
      },
      center: {
        lat: -6.93,
        lng: 107.6,
      },
      center2: {
        lat: -6.93,
        lng: 107.6,
      },
      currentPlace: null,
      markers: [],
      markers2: [],
      places: [],
    };
  },
  components: { GoogleMap, Marker },
  created() {
    this.loadPersonel();
    this.loadAbsensiLembur();
    this.getDateNow();
  },
  mounted() {
    // this.geolocate();
    setTimeout(() => {
      this.table = $("#dt-overtime").DataTable({ autoWidth: false });
    }, 1000);
  },
  methods: {
    getDateNow() {
      const today = new Date();
      this.search.t_absensi_Dates = moment(today).format("YYYY-MM-DD");
    },
    mapStatus(status) {
      switch (status) {
        case 0:
          return '<span class="text-muted">Menunggu Persetujuan</span>';
          break;
        case 1:
          return '<span class="text-success">Disetujui</span>';
          break;
        case 2:
          return '<span class="text-danger">Ditolak</span>';
          break;
        case 3:
          return '<span class="text-warning">Kadaluarsa</span>';
          break;
        default:
          return '-'
          break;
      }
    },
    overtimeApprove(type = null) {
      if (type != null) {
        this.data.id = this.id;
        this.data.type = type;
      }
      $(".btn-submit").attr("disabled", true);
      // $('.btn-submit').attr('disabled', true)
      axios
        .post(env.VITE_API_URL + "overtime/approve", this.data)
        .then((response) => {
          this.loadAbsensiLembur();
          let status = response.data.status;
          let message = response.data.message;
          let status_message =
            status == Api.STATUS_SUCCESS ? Api.MES_SUCESS : Api.MES_ERROR;
          Toast.fire({
            icon: status_message,
            title: message,
          });
          $(".btn-submit").attr("disabled", false);
          $(".btn-cancel").trigger("click");
        })
        .catch((e) => {
          Api.messageError(e);
          $(".btn-submit").attr("disabled", false);
        });
    },
    menitLembur(val) {
      var start = val.t_absensi_startDate + " " + val.t_absensi_startClock;
      var end = val.t_absensi_endDate + " " + val.t_absensi_endClock;
      var a = moment(moment(start).toArray());
      var b = moment(moment(end).toArray());
      var result = b.diff(a, "minutes");
      return (result < 0 ? 0 : result) + " Menit";
    },
    filterRangeDate() {
      if (this.filterType == "show") {
        var a = moment(moment(this.search.startDate).toArray());
        var b = moment(moment(this.search.endDate).toArray());
        if (b.diff(a, "days") + 1 < 8) {
          this.loadAbsensiLembur();
        } else {
          Swal.fire({
            title: "Perhatian",
            text: "Selisih tanggal melebihi dari 7 hari harap gunakan unduh",
            icon: "warning",
            confirmButtonColor: "#3085d6",
            confirmButtonText: "OK",
          });
        }
      } else {
        Swal.fire({
          title: "Ekspor Lembur",
          text:
            "Ingin Ekspor Lembur Dengan Tanggal " +
            this.search.startDate +
            " Sampai " +
            this.search.endDate +
            " ?",
          icon: "warning",
          showCancelButton: true,
          confirmButtonColor: "#3085d6",
          cancelButtonColor: "#d33",
          confirmButtonText: "OK",
          reverseButtons: true,
        }).then((resp) => {
          if (resp.isConfirmed) {
            axios
              .get(
                `${env.VITE_API_URL}/exports/lembur?start_date=${this.search.startDate}&end_date=${this.search.endDate}`
              )
              .then((resp) => {
                if (resp.status == 200) {
                  if (Api.response(resp.data) === Api.STATUS_SUCCESS) {
                    window.location.href = resp.data.url;
                  }
                }
              });
          }
        });
      }
    },
    loadPersonel() {
      this.$Progress.start();
      axios
        .get(env.VITE_API_URL + "index-data-personel")
        .then((response) => {
          if (Api.response(response.data, false) === Api.STATUS_SUCCESS) {
            this.$Progress.finish();
            this.personels = response.data.data;
          }
        })
        .catch((e) => {
          this.$Progress.fail();
          Api.messageError(e);
        });
    },
    loadAbsensiLembur() {
      this.$Progress.start();
      axios
        .get(env.VITE_API_URL + "overtime", { params: this.search })
        .then((response) => {
          this.$Progress.finish();
          // console.log(response);
          if (Api.response(response.data, false) === Api.STATUS_SUCCESS) {
            this.overtimes = response.data.data;
            this.table.destroy();
            this.$nextTick(() => {
              this.table = $("#dt-overtime").DataTable({ autoWidth: false });
            });
          }
        })
        .catch((e) => {
          this.$Progress.fail();
          Api.messageError(e);
        });
    },
    Modal(val, type = "start") {
      this.modal = {
        title: "Perbarui Lembur",
        id: val.id_t_absensi,
        personel: val.personel.id_m_personel,
        nama: val.personel.m_personel_names,
        startClock: val.t_absensi_startClock,
        endClock: val.t_absensi_endClock,
        startDate: val.t_absensi_startDate,
        endDate: val.t_absensi_endDate,
        catatan: val.t_absensi_catatan,
        status_admin: val.t_absensi_status_admin,
        catatan_admin: val.t_absensi_catatan_admin,
      };
      if (val.t_absensi_status == 2) {
        this.modal.status = true;
      }
      if (val.photo_absensi.length > 0) {
        if (val.photo_absensi.length > 1) {
          this.modal.photo2 =
            Api.URL_PHOTO + val.photo_absensi[1].t_absensi_photofileOri;
        }
        this.modal.photo =
          Api.URL_PHOTO + val.photo_absensi[0].t_absensi_photofileOri;
      }


    },
    ModalApprove(val, type = "start") {
      this.data = {
        id: val.id_t_absensi,
        type: type,
      };
    },
    ModalReset() {
      this.modal = {
        title: "Tambah Lembur",
        id: "",
        nama: "",
        startDate: "",
        startClock: "",
        endDate: "",
        endClock: "",
        personel: "",
      };
    },
    Maps(val, status) {
      this.locations = [];
      var latLong = val.t_absensi_latLong.split(",");
      modal.title = "Mulai";
      if (status == "end") {
        modal.title = "Selesai";
        latLong = val.t_absensi_latLongEnd.split(",");
      }
      this.center = {
        lat: parseFloat(latLong[0]),
        lng: parseFloat(latLong[1]),
      };
      this.locations.push(this.center);
    },
    resetEndDate() {
      this.search.endDate = "";
    },
    updateAbsensi() {
      $("#submit").prop("disabled", true);
      $("#submit").html("Loading...");
      axios
        .post(env.VITE_API_URL + "overtime", {
          id: this.modal.id,
          absensi: this.modal,
        })
        .then((response) => {
          if (Api.response(response.data) === Api.STATUS_SUCCESS) {
            $("#Modal").modal("hide");
            this.loadAbsensiLembur();
          }
          $("#submit").prop("disabled", false);
          $("#submit").html("Submit");
        })
        .catch((e) => {
          Api.messageError(e);
          $("#submit").prop("disabled", false);
        });
    },
    confirmDelete(id, name) {
      return Api.confirmDelete(
        "Apakah anda yakin?",
        "Absensi " + name + " akan dihapus!"
      ).then((result) => {
        if (result.isConfirmed) {
          this.deleteAbsensiLembur(id);
        }
      });
    },
    deleteAbsensiLembur(id) {
      axios
        .delete(env.VITE_API_URL + "overtime/" + id)
        .then((response) => {
          this.loadAbsensiLembur();
          let status = response.data.status;
          let message = response.data.message;
          let status_message =
            status == Api.STATUS_SUCCESS ? Api.MES_SUCESS : Api.MES_ERROR;
          Toast.fire({
            icon: status_message,
            title: message,
          });
        })
        .catch((e) => {
          Api.messageError(e);
        });
    },
    addMarker() {
      this.markers = [];
      const marker = {
        lat: this.center.lat,
        lng: this.center.lng,
      };
      this.markers.push({ position: marker });
    },
    addMarker2() {
      this.markers2 = [];
      const marker = {
        lat: this.center2.lat,
        lng: this.center2.lng,
      };
      this.markers2.push({ position: marker });
    },
    geolocate: function () {
      navigator.geolocation.getCurrentPosition((position) => {
        this.center = {
